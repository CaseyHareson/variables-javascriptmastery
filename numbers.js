// NUMBERS

// Creating variables with a number value.
// In JavaScript variables do not use "int" and "float". Use "const" and "let" instead.
const wholeNumber = 5; 
const decimalNumber = 0.5; 

// Creating variables to perform mathematical operations.
const firstNumber = 5;
const secondNumber = 10; 

// Show in console numbers with whole and decimal values.
console.log(`Whole number: ${wholeNumber}`);
console.log(`Decimal number: ${decimalNumber}`);

//Show in console results of mathematical operations.
console.log(`Addition: ${firstNumber + secondNumber}`);
console.log(`Subtraction: ${firstNumber - secondNumber}`);
console.log(`Multiplication: ${firstNumber * secondNumber}`);
console.log(`Division: ${firstNumber / secondNumber}`);

