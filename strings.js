// STRINGS

// Creating a string using single quotation marks.
const singleQuotes = 'Hello!';

// Creating a string using double quotation marks.
const doubleQuotes = "Hello!!";

// Creating a string using backticks. 
const name = `John`;
// Backticks allow strings to include variables.
const backticks = `Hello, ${name}. Welcome!!!`;

// Console output values from strings.
// Show in console the type of value.
console.log(singleQuotes, typeof singleQuotes);
console.log(doubleQuotes, typeof doubleQuotes);
console.log(backticks, typeof backticks);
