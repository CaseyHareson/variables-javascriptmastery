//NULL VALUE
// Null has a special value equal to nothing or unknown.

// Creat a variable with "Null" value.
let age = null;

// Show value Null in console.
console.log(`Variable "age" is equal: ${age}`);
// Show type of the variable. Null is an object.
console.log(`Type of variable "age": ${typeof age}`);

// Null value can be reassigned.
age = 20;

// Show reassigned value.
console.log(`Reassigne value to number: ${age}`);

// Show type of reassigned variable.
console.log(`Type of variable "age": ${typeof age}`);
