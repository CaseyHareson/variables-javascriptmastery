// UNDEFINED VALUE
// Undefined is a separate type of value.
// Undefined means a variable has been created, but unassigned.

// Create a variable with no value.
let x;

// Show the value of "x" in console.
console.log(`Variable "x" is equal: ${x}`);
// Show type of indefined variable "x". 
console.log(`The type of variable "x" is:  ${typeof x}`);
